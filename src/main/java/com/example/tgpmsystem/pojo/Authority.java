package com.example.tgpmsystem.pojo;


public class Authority {

  private String authoId;
  private String authoNo;
  private String authoUrl;
  private String authoName;


  public String getAuthoId() {
    return authoId;
  }

  public void setAuthoId(String authoId) {
    this.authoId = authoId;
  }


  public String getAuthoNo() {
    return authoNo;
  }

  public void setAuthoNo(String authoNo) {
    this.authoNo = authoNo;
  }


  public String getAuthoUrl() {
    return authoUrl;
  }

  public void setAuthoUrl(String authoUrl) {
    this.authoUrl = authoUrl;
  }


  public String getAuthoName() {
    return authoName;
  }

  public void setAuthoName(String authoName) {
    this.authoName = authoName;
  }

}
