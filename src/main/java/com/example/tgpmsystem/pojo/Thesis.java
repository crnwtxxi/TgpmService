package com.example.tgpmsystem.pojo;

import java.util.Date;
public class Thesis {

  private String thesisId;
  private String thesisTitle;
  private Date thesisDate;
  private String thesisPeriod;
  private String thesisName;
  private String thesisSeque;
  private String thesisType;
  private String thesisLevel;
  private String thesisDesc;
  private String thesisStatus;


  public String getThesisId() {
    return thesisId;
  }

  public void setThesisId(String thesisId) {
    this.thesisId = thesisId;
  }


  public String getThesisTitle() {
    return thesisTitle;
  }

  public void setThesisTitle(String thesisTitle) {
    this.thesisTitle = thesisTitle;
  }


  public Date getThesisDate() {
    return thesisDate;
  }

  public void setThesisDate(Date thesisDate) {
    this.thesisDate = thesisDate;
  }


  public String getThesisPeriod() {
    return thesisPeriod;
  }

  public void setThesisPeriod(String thesisPeriod) {
    this.thesisPeriod = thesisPeriod;
  }


  public String getThesisName() {
    return thesisName;
  }

  public void setThesisName(String thesisName) {
    this.thesisName = thesisName;
  }


  public String getThesisSeque() {
    return thesisSeque;
  }

  public void setThesisSeque(String thesisSeque) {
    this.thesisSeque = thesisSeque;
  }


  public String getThesisType() {
    return thesisType;
  }

  public void setThesisType(String thesisType) {
    this.thesisType = thesisType;
  }


  public String getThesisLevel() {
    return thesisLevel;
  }

  public void setThesisLevel(String thesisLevel) {
    this.thesisLevel = thesisLevel;
  }


  public String getThesisDesc() {
    return thesisDesc;
  }

  public void setThesisDesc(String thesisDesc) {
    this.thesisDesc = thesisDesc;
  }


  public String getThesisStatus() {
    return thesisStatus;
  }

  public void setThesisStatus(String thesisStatus) {
    this.thesisStatus = thesisStatus;
  }

}
